    {
        static void Main(string[] args)
        {
            // Hello world code
            Console.WriteLine("Hello, World!");

            // Draw a shape
            Console.WriteLine("   /|");
            Console.WriteLine("  / |");
            Console.WriteLine(" /  |");
            Console.WriteLine("/___|");

            // Variables
            Console.WriteLine("There once was a man George");
            Console.WriteLine("He is 70 years old");
